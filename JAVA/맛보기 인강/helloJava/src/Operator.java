public class Operator {
    public static void main(String[] args) {
        int age = 40;
        int rnt;

        rnt = 1 + 2;
        System.out.println(rnt);
        System.out.println("--------");


        int a = 15, b = 4;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
        System.out.println("--------");


        int c = 10;
        int d = 10;

        System.out.println(c++);
        System.out.println(c);
        System.out.println(d--);
        System.out.println(d);


    }
}
